///تقوم باختصار النصوص الطويلة [subtractText] دالة
///
/// وإضافة "..." إذا تجاوزت الحد الأقصى للطول
///
/// 📌 وظيفة الكود
///
/// 🔹("")تُرجع نصًا فارغًا null إذا كان النص
///
/// 🔹يتم قصه [maxLength] إذا كان طول النص أكبر من
///
///  وإضافة "..." في النهاية.
///
/// 🔹 إذا كان النص قصيرًا بما يكفي، يُعاد كما هو دون تعديل.
///
/// 📌 المخرجات المحتملة
///
/// ✅ "Hello World" ← إذا كان النص قصيرًا.
///
/// ✅ "This is a very long te..." ← (مثل 20 حرفًا) maxLength إذا كان النص أطول من
///
/// ✅ "" ← null إذا كان النص
///
/// 🚀 يساعد هذا الكود في عرض النصوص بطريقة مرتبة دون استهلاك مساحة كبيرة في الواجهة! 🚀
// ignore_for_file: dangling_library_doc_comments

subtractText(String? text, {int maxLength = 20}) {
  return (text == null)
      ? ""
      : text.length > maxLength
          ? "${text.substring(0, maxLength)}..."
          : text;
}
